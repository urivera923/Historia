          <p class="card-text">Originada en la mitología de Japón nació una leyenda llamada «Kuchisake-Onna» en español conocida como «La mujer con la boca cortada». Se cuenta sobre una mujer que fue asesinada y desfigurada por su esposo, convirtiéndose en un Yokai (espíritu demoníaco) que regresó para vengarse. <br> <br>
            Hace mucho tiempo había una preciosa mujer que se casó con un Samurái. A pesar de su matrimonio seguían siendo muchos los que la amaban y pretendían debido a su hermosura, lo cual hizo crecer su amor propio volviéndola vanidosa y despreocupada, pues cada vez que quería engañaba a su marido, volviéndolo ya una costumbre. El valiente guerrero Japonés al saber de las infidelidades de su esposa se vio sumido en un ataque de celos y furia, pensando en la deshonra causada por aquella mujer se abalanzándose sobre ella, le cortó la boca de un lado a otro mientras gritaba: – ¿Piensas que eres hermosa? ¿Quién va a pensar que eres hermosa ahora?- <br> <br>
            Desde aquel preocupante día se ve vagar por las calles de Japón, una mujer con la cara tapada por una máscara como la que usan los cirujanos. Cuando se topa con alguien les pregunta sin descubrirse el rostro:  – ¿Atashi kirei? (¿Soy hermosa?) –. La gente normalmente ve sus lindos ojos, y sus refinados rasgos y responden – Si –, sin hacer mucho caso a la máscara, pues en Japón su uso es muy frecuente para evitar enfermedades y no respirar el aire contaminado. Al obtener la respuesta antes mencionada, la mujer retira la máscara dejando al descubierto la horrible hendidura que se extiende de oreja a oreja con una escalofriante sonrisa. Y les pregunta de nuevo: – ¿Kore Demo? (¿Y ahora?) -. Todo aquel que se asusta, grita o muestra el miedo en su cara es atacado por el espíritu que con unas tijeras gigantes les corta la cabeza, ni que decir de los valientes que se atreven a responder – No –. <br> <br>
            Sí la víctima responde de nuevo que sí solamente le cortará la boca de lado a lado para que sufra su misma suerte. Pareciera entonces que es imposible escapar de Kuchisake-Onna, puedes salir corriendo pero aparecerá frente a ti de nuevo, o estará esperándote tras la puerta de tu casa para asesinarte. <br> <br>
            Por fortuna no todo está perdido, se dice que ante su interrogante puedes contestar con otra pregunta: – ¿Y yo? ¿Soy hermoso? – Eso confundirá al espíritu que se quedará pensativo dándote tiempo a escapar. También puedes llevar dulces contigo y se los ofreces antes de dejarla hablar, quedará contenta con el regalo y te permitirá marcharte. <br>
